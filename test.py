# Cuboid volume and surface area


a = int(3)
b = int(4)
c = int(5)

volume = a * b * c # Formula for volume: V = abc

print (str(volume) + ("cm cubed"))

surface_area = (2 * (a * b)) + (2 * (b * c)) + (2 * (a * c)) # Formula for surface area S = 2ab + 2bc +2ac

print (str(surface_area) + ("cm squared"))

